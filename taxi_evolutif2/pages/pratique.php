<h1>Les taxis de New-York</h1>
	<p>
		Que l'on vive en permanence à New-York ou que l'on y soit temporairement pour affaire, on mesure vite l'efficacité et la rapidité du système 
		des taxis new-yorkais. Et un séjour, même très court, à New York ne serait pas complet sans faire au moins une fois l'expérience d'un Yellow Cab. 
		Inlassablement, du soir au matin et du matin au soir, ces taxis légendaires arpentent Manhattan dans le sens transversal et longitudinal. 
		Il suffit de lever le bras pour en trouver un. Lorsqu'un taxi est libre, l'inscription " on duty " est allumée sur le toit. Les nom, numéro et photo 
		du taxi driver sont affichés à l'avant du véhicule ; bon moyen de savoir si votre chauffeur est francophone ou non. A partir de deux personnes,
		le taxi est le moye
		
		
		
		
		n de transport le plus économique pour les petites et moyennes distances. Ceci est d'autant plus vrai lorsqu'on est trois ou bien 
		quatre. Au-delà, il faut héler un deuxième taxi vu qu'il est formellement interdit, pour des raisons d'assurances, de monter à plus de quatre dans un 
		taxi qui ne soit pas une limousine. Pour connaître les règles en détail, nous vous invitons à vous connecter sur le site du New York Taxi and 
		Limousine Commission .
	</p>
	<p>
				Les tarifs sont inscrits sur les portières extérieures. Le compteur affiche systématiquement $2.50 au moment où l'on monte dans un taxi, puis sont 
				chargés $0.40 à chaque minute sans mouvement ou chaque 1/5 de mile. La nuit, il y a un supplément forfaitaire de $0.50. Pour donner un ordre d'idée, 
				une prise en charge d'une vingtaine de blocs dans un trafic fluide revient à environ $6. De Manhattan (Midtown) vers Queens (Forest Hills, Rego Park, 
				Roosevelt, etc.), il faut compter $18. Les péages éventuels sont toujours à la charge du client. Il est d'usage de donner un pourboire équivalent à 15% 
				du prix de la course. Cependant pour les petites distances, certains clients arrondissent parfois au dollar supérieur ou laissent un dollar supplémentaire 
				sans se lancer dans de savants calculs. Pour tout savoir sur les tarifs, nous vous invitons à vous connecter sur le site du New York Taxi and Limousine Commission.
				</p>
				<p>
				Excepté lorsqu'ils se trouvent dans les aéroports, les Yellow Cabs ne peuvent charger de clients hors des limites de Manhattan. Dans les autres boroughs, 
				on a recours à des compagnies indépendantes ( leur liste figure dans les Yellow Pages à la rubrique " Car Service " ). Ces dernières fonctionnent dans 
				leur grande majorité davantage au forfait qu'au compteur kilométrique ( nous devrions dire " mileométrique " ). A Manhattan, ne prenez QUE des Yellow Cabs.
				</p>
